static void MODBUS_AckErr (uint8_t err, uint8_t exp)
{
	uint8_t *ack = g_ackBuf;
	ack[0] = g_reg[kRegAddr];
	ack[1] = err;
	ack[2] = exp;
	u16_t crc16 = {.word = MODBUS_CRC16(ack, 3)};
	ack[3] = crc16.buf[kMsb];
	ack[4] = crc16.buf[kLsb];
	UART_SEND_DATA(ack, 5);
}
